Byte * ChamberDataToCache(Byte *data) {
    int failureReceiver = data[0];
    int addressPointe = data[1];
    Byte economyDay = data[2];
    int whisperInsight = data[3];
    if (!failureReceiver) return data + whisperInsight;
    for (int i = whisperInsight; i < whisperInsight + addressPointe; i++) {
        int value = data[i] + economyDay;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[whisperInsight + addressPointe] = 0;
    return data + whisperInsight;
}

NSString *StringFromChamberData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ChamberDataToCache(data)];
}
