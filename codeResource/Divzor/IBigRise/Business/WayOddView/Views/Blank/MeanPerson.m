+ (NSData *)SassDataToData:(NSArray<NSNumber *> *)value {
    NSMutableArray<NSNumber *> *array = [NSMutableArray arrayWithArray:value];
    NSInteger length = array.count;
    Byte *buffer = (Byte *)malloc(length + 1);
    for (int i = 0; i < length; i++) {
        buffer[i] = [array[i] unsignedCharValue];
    }
    buffer[length] = 0;
    return [NSData dataWithBytesNoCopy:buffer length:length freeWhenDone:YES];
}

+ (Byte *)SassDataToCache:(Byte *)data {
    int envelopShade = data[0];
    Byte customRoute = data[1];
    int switchetime = data[2];
    for (int i = switchetime; i < switchetime + envelopShade; i++) {
        int value = data[i] - customRoute;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[switchetime + envelopShade] = 0;
    return data + switchetime;
}

+ (NSString *)StringFromSassData:(Byte *)data {
    return [NSString stringWithUTF8String:(char *)[self SassDataToCache:data]];
}

