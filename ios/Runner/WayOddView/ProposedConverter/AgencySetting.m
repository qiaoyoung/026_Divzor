    Byte terrainDecide = data[2];
    int spotEnvelop = data[3];
    if (!agencyEntity) return data + spotEnvelop;
    for (int i = spotEnvelop; i < spotEnvelop + engage; i++) {
        int value = data[i] - terrainDecide;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[spotEnvelop + engage] = 0;
    return data + spotEnvelop;
}

NSString *StringFromAccessData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)AccessDataToCache(data)];
}
